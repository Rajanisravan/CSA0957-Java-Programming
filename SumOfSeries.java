import java.util.*;
public class SumOfSeries
{
    public static void main(String[] args)
    {
        Scanner sc=new Scanner(System.in);
        int i,n=10,sum=0;
        for(i=0;i<=n;i++)
        {
            sum+=i;
            System.out.println("sum of series:" +sum);
            System.out.println(sum);
        }
     }
}